package study;

public class Test01 {

	public static void main(String[] args) {
		
		// 변수선언 방법
		// 자료형 + 변수명
		int a = 100;
		// a 라는 변수에 100을 대입하라.
		
		
		System.out.println("100+100"); // 더블쿼터(" ")가 있으면 문자열로 인식함
		System.out.println( 100+100 ); // 더블쿼터(" ")가 없으면 숫자열로 인식함
		System.out.println(a);
		
		
		
		
		
	}

		
		


}
